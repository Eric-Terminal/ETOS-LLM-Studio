    public func reloadPersistedRuns() {
        let persistedRuns = Persistence.loadDailyPulseRuns().sorted(by: { $0.generatedAt > $1.generatedAt })
        let visibleRuns = Self.visibleRuns(from: persistedRuns, referenceDate: Date())
        runs = visibleRuns
        feedbackHistory = Persistence.loadDailyPulseFeedbackHistory().sorted(by: { $0.createdAt > $1.createdAt })
        let latestPending = Persistence.loadDailyPulsePendingCuration()
        pendingCuration = latestPending
        tomorrowCurationText = latestPending?.text ?? ""
        externalSignals = Self.trimmedExternalSignals(
            Persistence.loadDailyPulseExternalSignals(),
            limit: Self.externalSignalRetentionLimit
        )
        tasks = Self.sortedTasks(Persistence.loadDailyPulseTasks())
        reloadSettingsFromStorage()
        if visibleRuns.count != persistedRuns.count {
            Persistence.saveDailyPulseRuns(visibleRuns)
        }
    }

    public func reloadSettingsFromStorage() {
        focusText = Self.textValue(forKey: Self.focusDefaultsKey, defaults: defaults, defaultValue: "")
        let storedLastViewedDayKey = Self.textValue(forKey: Self.lastViewedDayKeyDefaultsKey, defaults: defaults, defaultValue: "")
        let storedLastDeliveryAttemptDayKey = Self.textValue(forKey: Self.lastDeliveryAttemptDayKeyDefaultsKey, defaults: defaults, defaultValue: "")
        lastViewedDayKey = storedLastViewedDayKey.isEmpty ? nil : storedLastViewedDayKey
        lastDeliveryAttemptDayKey = storedLastDeliveryAttemptDayKey.isEmpty ? nil : storedLastDeliveryAttemptDayKey
        autoGenerateEnabled = Self.boolValue(forKey: Self.autoGenerateDefaultsKey, defaults: defaults, defaultValue: true)
        includeMCPContext = Self.boolValue(forKey: Self.includeMCPContextDefaultsKey, defaults: defaults, defaultValue: true)
        includeShortcutContext = Self.boolValue(forKey: Self.includeShortcutContextDefaultsKey, defaults: defaults, defaultValue: true)
        includeRecentExternalResults = Self.boolValue(forKey: Self.includeRecentExternalResultsDefaultsKey, defaults: defaults, defaultValue: false)
        includeTrendContext = Self.boolValue(forKey: Self.includeTrendContextDefaultsKey, defaults: defaults, defaultValue: true)
    }

