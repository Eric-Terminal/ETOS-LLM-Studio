    public init(
        chatService: ChatService,
        memoryManager: MemoryManager,
        defaults: UserDefaults = .standard
    ) {
        self.chatService = chatService
        self.memoryManager = memoryManager
        self.defaults = defaults
        self.retentionLimit = Self.persistedRetentionLimit
        let persistedRuns = Persistence.loadDailyPulseRuns().sorted(by: { $0.generatedAt > $1.generatedAt })
        let pendingCuration = Persistence.loadDailyPulsePendingCuration()
        self.runs = Self.visibleRuns(from: persistedRuns, referenceDate: Date())
        self.feedbackHistory = Persistence.loadDailyPulseFeedbackHistory().sorted(by: { $0.createdAt > $1.createdAt })
        self.pendingCuration = pendingCuration
        self.externalSignals = Self.trimmedExternalSignals(Persistence.loadDailyPulseExternalSignals(), limit: Self.externalSignalRetentionLimit)
        self.tasks = Self.sortedTasks(Persistence.loadDailyPulseTasks())
        self.focusText = Self.textValue(forKey: Self.focusDefaultsKey, defaults: defaults, defaultValue: "")
        self.tomorrowCurationText = pendingCuration?.text ?? ""
        let storedLastViewedDayKey = Self.textValue(forKey: Self.lastViewedDayKeyDefaultsKey, defaults: defaults, defaultValue: "")
        let storedLastDeliveryAttemptDayKey = Self.textValue(forKey: Self.lastDeliveryAttemptDayKeyDefaultsKey, defaults: defaults, defaultValue: "")
        self.lastViewedDayKey = storedLastViewedDayKey.isEmpty ? nil : storedLastViewedDayKey
        self.lastDeliveryAttemptDayKey = storedLastDeliveryAttemptDayKey.isEmpty ? nil : storedLastDeliveryAttemptDayKey
        self.autoGenerateEnabled = Self.boolValue(forKey: Self.autoGenerateDefaultsKey, defaults: defaults, defaultValue: true)
        self.includeMCPContext = Self.boolValue(forKey: Self.includeMCPContextDefaultsKey, defaults: defaults, defaultValue: true)
        self.includeShortcutContext = Self.boolValue(forKey: Self.includeShortcutContextDefaultsKey, defaults: defaults, defaultValue: true)
        self.includeRecentExternalResults = Self.boolValue(forKey: Self.includeRecentExternalResultsDefaultsKey, defaults: defaults, defaultValue: false)
        self.includeTrendContext = Self.boolValue(forKey: Self.includeTrendContextDefaultsKey, defaults: defaults, defaultValue: true)
        prunePendingCurationIfNeeded(referenceDate: Date())
        if runs.count != persistedRuns.count {
            Persistence.saveDailyPulseRuns(runs)
        }
        if externalSignals.count != Persistence.loadDailyPulseExternalSignals().count {
            Persistence.saveDailyPulseExternalSignals(externalSignals)
        }
        if tasks != Self.sortedTasks(Persistence.loadDailyPulseTasks()) {
            Persistence.saveDailyPulseTasks(tasks)
        }

        syncNotificationObserver = NotificationCenter.default.addObserver(
            forName: .syncDailyPulseUpdated,
            object: nil,
            queue: .main
        ) { [weak self] _ in
            Task { @MainActor [weak self] in
                self?.reloadPersistedRuns()
            }
        }
    }

    deinit {
        if let syncNotificationObserver {
            NotificationCenter.default.removeObserver(syncNotificationObserver)
        }
    }

    private static func usesDatabase(defaults: UserDefaults) -> Bool {
        defaults === UserDefaults.standard
    }

    private static func boolValue(forKey key: String, defaults: UserDefaults, defaultValue: Bool) -> Bool {
        guard usesDatabase(defaults: defaults) else {
            return defaults.object(forKey: key) as? Bool ?? defaultValue
        }
        if let stored = Persistence.readAppConfigInteger(key: key) {
            return stored != 0
        }
        guard defaults.object(forKey: key) != nil else { return defaultValue }
        let legacy = defaults.bool(forKey: key)
        if Persistence.writeAppConfig(key: key, integer: legacy ? 1 : 0, typeHint: "bool") {
            defaults.removeObject(forKey: key)
        }
        return legacy
    }

    private static func textValue(forKey key: String, defaults: UserDefaults, defaultValue: String) -> String {
        guard usesDatabase(defaults: defaults) else {
            return defaults.string(forKey: key) ?? defaultValue
        }
        if let stored = Persistence.readAppConfigText(key: key) {
            return stored
        }
        guard let legacy = defaults.string(forKey: key) else { return defaultValue }
        if Persistence.writeAppConfig(key: key, text: legacy, typeHint: "text") {
            defaults.removeObject(forKey: key)
        }
        return legacy
    }

    private static func save(_ value: Bool, forKey key: String, defaults: UserDefaults) {
        guard usesDatabase(defaults: defaults) else {
            defaults.set(value, forKey: key)
            return
        }
        if Persistence.writeAppConfig(key: key, integer: value ? 1 : 0, typeHint: "bool") {
            defaults.removeObject(forKey: key)
        }
    }

    private static func save(_ value: String, forKey key: String, defaults: UserDefaults) {
        guard usesDatabase(defaults: defaults) else {
            defaults.set(value, forKey: key)
            return
        }
        if Persistence.writeAppConfig(key: key, text: value, typeHint: "text") {
            defaults.removeObject(forKey: key)
        }
    }

    public var latestRun: DailyPulseRun? {
        runs.sorted(by: { $0.generatedAt > $1.generatedAt }).first
    }

    public var todayRun: DailyPulseRun? {
        runs.first(where: { $0.dayKey == Self.dayKey(for: Date()) })
    }

    public var archivedRuns: [DailyPulseRun] {
        []
    }

    public var primaryRun: DailyPulseRun? {
        todayRun ?? latestRun
    }

    internal func notificationTarget(
        runID: UUID?,
        cardID: UUID?,
        dayKey: String?
    ) -> (runID: UUID, card: DailyPulseCard)? {
        if let runID,
           let run = runs.first(where: { $0.id == runID }) {
            if let cardID,
               let card = run.cards.first(where: { $0.id == cardID }) {
                return (runID, card)
            }
            if let fallbackCard = run.visibleCards.first ?? run.cards.first {
                return (runID, fallbackCard)
            }
        }

        let candidateRuns: [DailyPulseRun]
        if let dayKey, !dayKey.isEmpty {
            candidateRuns = runs
                .filter { $0.dayKey == dayKey }
                .sorted(by: { $0.generatedAt > $1.generatedAt })
        } else if let primaryRun {
            candidateRuns = [primaryRun]
        } else {
            candidateRuns = runs.sorted(by: { $0.generatedAt > $1.generatedAt })
        }

        for run in candidateRuns {
            if let fallbackCard = run.visibleCards.first ?? run.cards.first {
                return (run.id, fallbackCard)
            }
        }
        return nil
    }

